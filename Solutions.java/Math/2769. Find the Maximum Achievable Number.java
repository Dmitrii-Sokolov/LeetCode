/// <summary>
/// 2769. Find the Maximum Achievable Number
/// https://leetcode.com/problems/find-the-maximum-achievable-number
/// 
/// Difficulty Easy
/// Acceptance 90.5%
/// 
/// Math
/// </summary>
class Solution
{
  public int theMaximumAchievableX(int num, int t)
  {
    return num + 2 * t;
  }
}
